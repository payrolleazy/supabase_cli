create table if not exists public.sim_fixture_execution_audit (
  id bigint generated by default as identity primary key,
  fixture_run_key text not null,
  phase text not null,
  suite_run_id uuid null,
  workflow_run_id uuid null,
  module text null,
  execution_mode text null,
  requested_by_role text null,
  requested_by_uid uuid null,
  sql_sha256 text not null,
  success boolean not null,
  result jsonb not null default '{}'::jsonb,
  error_message text null,
  created_at timestamptz not null default timezone('utc', now()),
  constraint sim_fixture_execution_audit_phase_check check (phase in ('SEED', 'CLEANUP')),
  constraint sim_fixture_execution_audit_result_check check (jsonb_typeof(result) = 'object')
);

