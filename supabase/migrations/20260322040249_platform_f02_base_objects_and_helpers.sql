create or replace function public.platform_clone_registered_template_tables(
  p_tenant_id uuid,
  p_template_version text
)
returns jsonb
language plpgsql
security definer
set search_path to 'public', 'pg_temp'
as $function$
declare
  v_schema_name text;
  v_row record;
  v_created_tables integer := 0;
  v_existing_tables integer := 0;
  v_seeded_tables integer := 0;
  v_target_has_rows boolean;
  v_seeded_row_count bigint;
begin
  select schema_name
  into v_schema_name
  from public.platform_tenant
  where tenant_id = p_tenant_id;

  if v_schema_name is null then
    raise exception 'tenant % has no schema_name bound', p_tenant_id;
  end if;

  if not public.platform_schema_exists(v_schema_name) then
    raise exception 'schema % does not exist for tenant %', v_schema_name, p_tenant_id;
  end if;

  for v_row in
    select *
    from public.platform_template_table_registry
    where template_version = p_template_version
      and clone_enabled = true
    order by clone_order, id
  loop
    if not public.platform_table_exists(v_row.source_schema_name, v_row.source_table_name) then
      raise exception 'template source table %.% not found for template version %',
        v_row.source_schema_name, v_row.source_table_name, p_template_version;
    end if;

    if not public.platform_table_exists(v_schema_name, v_row.target_table_name) then
      execute format(
        'create table %I.%I (like %I.%I including all)',
        v_schema_name,
        v_row.target_table_name,
        v_row.source_schema_name,
        v_row.source_table_name
      );
      v_created_tables := v_created_tables + 1;
    else
      v_existing_tables := v_existing_tables + 1;
    end if;

    if v_row.seed_mode = 'copy_all_rows' then
      execute format(
        'select exists (select 1 from %I.%I limit 1)',
        v_schema_name,
        v_row.target_table_name
      )
      into v_target_has_rows;

      if not v_target_has_rows then
        execute format(
          'insert into %I.%I select * from %I.%I',
          v_schema_name,
          v_row.target_table_name,
          v_row.source_schema_name,
          v_row.source_table_name
        );

        get diagnostics v_seeded_row_count = row_count;

        if v_seeded_row_count > 0 then
          v_seeded_tables := v_seeded_tables + 1;
        end if;
      end if;
    end if;
  end loop;

  return jsonb_build_object(
    'schema_name', v_schema_name,
    'template_version', p_template_version,
    'created_tables', v_created_tables,
    'existing_tables', v_existing_tables,
    'seeded_tables', v_seeded_tables
  );
end;
$function$;

create or replace view public.platform_schema_provisioning_view as
select
  pt.tenant_id,
  pt.tenant_code,
  pt.schema_name,
  pt.display_name,
  pt.tenant_kind,
  pp.provisioning_status,
  pp.schema_provisioned,
  pp.foundation_version,
  pp.ready_for_routing,
  pp.latest_completed_step,
  pp.last_error_code,
  pp.last_error_message,
  pp.last_error_at,
  public.platform_schema_exists(pt.schema_name) as schema_exists,
  coalesce(
    (
      select jsonb_agg(
        jsonb_build_object(
          'template_version', ptv.template_version,
          'schema_name', ptv.schema_name,
          'apply_status', ptv.apply_status,
          'applied_at', ptv.applied_at
        )
        order by ptv.applied_at nulls last, ptv.template_version
      )
      from public.platform_tenant_template_version ptv
      where ptv.tenant_id = pt.tenant_id
    ),
    '[]'::jsonb
  ) as applied_versions,
  (
    select jsonb_build_object(
      'run_id', pspr.run_id,
      'operation_kind', pspr.operation_kind,
      'template_version', pspr.template_version,
      'run_status', pspr.run_status,
      'started_at', pspr.started_at,
      'finished_at', pspr.finished_at,
      'error_code', pspr.error_code,
      'error_message', pspr.error_message,
      'details', pspr.details
    )
    from public.platform_schema_provisioning_run pspr
    where pspr.tenant_id = pt.tenant_id
    order by pspr.started_at desc, pspr.run_id desc
    limit 1
  ) as latest_run
from public.platform_tenant pt
join public.platform_tenant_provisioning pp
  on pp.tenant_id = pt.tenant_id;;
