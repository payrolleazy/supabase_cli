create or replace function public.platform_build_schema_name(p_tenant_code text)
returns text
language plpgsql
immutable
as $function$
declare
  v_tenant_code text := public.platform_normalize_tenant_code(p_tenant_code);
  v_candidate text;
begin
  if v_tenant_code is null then
    return null;
  end if;

  v_candidate := 'tenant_' || v_tenant_code;

  if length(v_candidate) <= 63 then
    return v_candidate;
  end if;

  return 'tenant_' || left(v_tenant_code, 63 - length('tenant_') - 9) || '_' || left(md5(v_tenant_code), 8);
end;
$function$;

create or replace function public.platform_schema_exists(p_schema_name text)
returns boolean
language sql
stable
as $function$
  select exists (
    select 1
    from pg_namespace
    where nspname = p_schema_name
  );
$function$;

create or replace function public.platform_table_exists(p_schema_name text, p_table_name text)
returns boolean
language sql
stable
as $function$
  select exists (
    select 1
    from information_schema.tables
    where table_schema = p_schema_name
      and table_name = p_table_name
      and table_type = 'BASE TABLE'
  );
$function$;

create or replace function public.platform_start_schema_provisioning_run(
  p_tenant_id uuid,
