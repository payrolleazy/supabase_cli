create table if not exists public.platform_template_version (
  template_version text primary key,
  template_scope text not null check (template_scope in ('foundation', 'module', 'patch')),
  template_status text not null check (template_status in ('draft', 'released', 'retired')),
  foundation_version text,
  description text,
  release_notes jsonb not null default '{}'::jsonb check (jsonb_typeof(release_notes) = 'object'),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  released_at timestamptz,
  created_by uuid
);

create table if not exists public.platform_template_table_registry (
  id bigint generated by default as identity primary key,
  template_version text not null references public.platform_template_version(template_version) on delete cascade,
  module_code text not null check (btrim(module_code) <> ''),
  source_schema_name text not null check (btrim(source_schema_name) <> ''),
  source_table_name text not null check (btrim(source_table_name) <> ''),
  target_table_name text not null check (btrim(target_table_name) <> ''),
  clone_order integer not null check (clone_order > 0),
  clone_enabled boolean not null default true,
  seed_mode text not null default 'none' check (seed_mode in ('none', 'copy_all_rows')),
  seed_filter jsonb not null default '{}'::jsonb check (jsonb_typeof(seed_filter) = 'object'),
  notes jsonb not null default '{}'::jsonb check (jsonb_typeof(notes) = 'object'),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  created_by uuid,
  constraint platform_template_table_registry_source_key unique (template_version, source_schema_name, source_table_name),
  constraint platform_template_table_registry_target_key unique (template_version, target_table_name)
);

create table if not exists public.platform_schema_provisioning_run (
  run_id uuid primary key,
  tenant_id uuid not null references public.platform_tenant(tenant_id) on delete cascade,
  schema_name text not null check (btrim(schema_name) <> ''),
  operation_kind text not null check (operation_kind in ('schema_create', 'template_apply', 'schema_repair')),
  template_version text,
  run_status text not null check (run_status in ('running', 'succeeded', 'failed')),
  started_at timestamptz not null default timezone('utc', now()),
  finished_at timestamptz,
  actor_user_id uuid,
  source text not null check (btrim(source) <> ''),
  error_code text,
  error_message text,
  details jsonb not null default '{}'::jsonb check (jsonb_typeof(details) = 'object')
);

create table if not exists public.platform_tenant_template_version (
  tenant_id uuid not null references public.platform_tenant(tenant_id) on delete cascade,
  template_version text not null references public.platform_template_version(template_version) on delete restrict,
  schema_name text not null check (btrim(schema_name) <> ''),
  apply_status text not null check (apply_status in ('pending', 'applied', 'failed')),
  applied_at timestamptz,
  run_id uuid references public.platform_schema_provisioning_run(run_id) on delete set null,
  details jsonb not null default '{}'::jsonb check (jsonb_typeof(details) = 'object'),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  primary key (tenant_id, template_version)
);

